// SHOW USER DASHBOARD
//////////////////////////////////////////////////////////////////////
// The `/view/user/dashboard` endpoint
router.get('/user/dashboard', withAuth, viewControls.getUserDashboard);

//////////////////////////////////////////////////////////////////////
// SHOW USER PROFILE
//////////////////////////////////////////////////////////////////////
// The `/view/user/profile` endpoint
router.get('/user/profile', withAuth, viewControls.getUserProfile);

